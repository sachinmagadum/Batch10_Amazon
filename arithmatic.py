def add(num1, num2):
    return num1 + num2


def sub(num1, num2):
    return num1 - num2


no1 = 20
no2 = 10
ans = add(no1, no2)
print(f"Addition of {no1} and {no2} is {ans}")

ans = sub(no1, no2)
print(f"Subtraction of {no1} and {no2} is {ans}")

def div(num1, num2):
    return num1 / num2

ans = div(no1, no2)
print(f"Division of {no1} and {no2} is {ans}")